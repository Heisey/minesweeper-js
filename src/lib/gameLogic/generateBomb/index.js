import gameLogic from '../'

/**
 * Generates a bomb in a random location
 * @param {*} params  Game Parameters
 * @param {*} board  MineSweep Game Board
 */
const generateBomb = (params, board) => {
  let emptyTile = true;

  while (emptyTile === true) {
    const tile = gameLogic.generateRandomTile(params)
    const row = tile[0]
    const col = tile[1]
    
    if (board[row][col]['bomb'] === false) {
      board[row][col]['bomb'] = true
      emptyTile = false
    }
  }
}

export default generateBomb