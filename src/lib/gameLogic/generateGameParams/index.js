/**
 * Generates amount of rows, cols, and bombs for game based 
 * on difficulty selected
 * @param {String} difficulty variable of 'easy', 'medium', or 'hard'
 */
const generateGameParams = (difficulty) => {
  const gameParams = {}

  if (difficulty === 'medium') {
    gameParams.difficulty = difficulty
    gameParams.rows = 16
    gameParams.cols = 16
    gameParams.bombs = 40
  } else if (difficulty === 'hard') {
    gameParams.difficulty = difficulty
    gameParams.rows = 30
    gameParams.cols = 16
    gameParams.bombs = 99
  } else {
    gameParams.difficulty = difficulty
    gameParams.rows = 9
    gameParams.cols = 9
    gameParams.bombs = 10
  }

  return gameParams
}

export default generateGameParams