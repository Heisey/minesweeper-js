import React from 'react';

// ???????????????????????? File Modules ??????????????????????????
// ?? Components
import GameBoard from '../../components/GameBoard';
import GameButton from '../../components/GameButton'
import InfoBox from '../../components/InfoBox'

// ?? Styles
import './GamePage.scss'

const GamePage = () => {

  const genenateBombs = (board) => {
    const row = Math.floor(Math.random() * 16)
    const column = Math.floor(Math.random() * 16)

    board[row][column]['bomb'] = true
  }

  const generateTiles = () => {
    const value = []

    for (let x = 0; x < 16; x++) {
      const tempArr = []
      for (let y = 0; y < 16; y++) {
        tempArr.push({
          grid: `${x}, ${y}`,
          bomb: false,
          number: 0
        })
      }
      value.push(tempArr)
    }

    for (let bombs = 0; bombs < 40; bombs++) {
      genenateBombs(value)
    }

    return value
  }

  

  return (
    <div className='GamePage'>
      <div className="GamePage__buttons">
        {/* // ?? Go Back to Main Menu */}
        <GameButton 
          icon="arrow-left"
        />

        {/* // ?? Restart Game */}
        <GameButton 
          icon="redo-alt"
        />

        {/* // ?? Show Game Instructions */}
        <GameButton 
          icon="question"
        />
      </div>

      <GameBoard 
        board={generateTiles()}
      />

      <div className="GamePage__infoBoxes">
        {/* // ?? Mines Left */}
        <InfoBox />

        {/* // ?? Time Accumulated */}
        <InfoBox />
      </div>
    </div>
  )
}

export default GamePage