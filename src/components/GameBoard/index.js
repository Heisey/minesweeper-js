// ?? Components
import GameTile from '../GameTile'

// ?? Styles
import './GameBoard.scss'

const GameBoard = (props) => {

  const {
    board,
    tileClickEvent
  } = props;

  return (
    <div className='GameBoard'>
      {board.map(row => row.map(tileObj => (
        <GameTile 
          key={`${tileObj.grid}`}
          info={tileObj}
          tileClickEvent={tileClickEvent}
        />
      )))}
    </div>
  )
}

export default GameBoard