import React from 'react';

// ???????????????????????? File Modules ??????????????????????????
// ?? Components
import GameTile from '../GameTile'

// ?? Styles
import './GameBoard.scss'

const GameBoard = (props) => {

  const {
    board,
    flagTile,
    tileClickEvent,
    difficulty,
    params
  } = props;

  const gameBoardClassNames = () => {
    let returnValue = 'GameBoard GameBoard'
    if (difficulty === 'medium') {
      returnValue = returnValue + '--medium'
    } else if (difficulty === 'hard') {
      returnValue = returnValue + '--hard'
    } else {
      returnValue = returnValue + '--easy'
    }

    return returnValue
  }

  return (
    <div className={gameBoardClassNames()}>
      {board.map(row => row.map(tileObj => (
        <GameTile 
          key={`${tileObj.grid}`}
          info={tileObj}
          flagTile={flagTile}
          tileClickEvent={tileClickEvent}
          params={params}
          board={board}
        />
      )))}
    </div>
  )
}

export default GameBoard