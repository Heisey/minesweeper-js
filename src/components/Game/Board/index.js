import React from 'react';
import { connect } from 'react-redux'

// ???????????????????????? File Modules ??????????????????????????
// ?? Components
import Game from '..'

// ?? Styles
import styles from './styles'

const Board = (props) => {

  const {
    board,
    flagTile,
    tileClickEvent,
  } = props;

  const {
    gameParams
  } = props

  const gameBoardClassNames = () => {
    let returnValue = 'GameBoard GameBoard'
    if (gameParams.difficulty === 'medium') {
      returnValue = returnValue + '--medium'
    } else if (gameParams.difficulty === 'hard') {
      returnValue = returnValue + '--hard'
    } else {
      returnValue = returnValue + '--easy'
    }

    return returnValue
  }

  return (
    <styles.Base
      difficulty={gameParams.difficulty}
    >
      {board.map(row => row.map(tileObj => (
        <Game.Tile 
          key={`${tileObj.grid}`}
          info={tileObj}
          flagTile={flagTile}
          tileClickEvent={tileClickEvent}
          board={board}
        />
      )))}
    </styles.Base>
  )
}

const mapStateToProps = (state) => {

  return {
    gameParams: state.gameLogic.gameParams
  }
}

export default connect(mapStateToProps)(Board)